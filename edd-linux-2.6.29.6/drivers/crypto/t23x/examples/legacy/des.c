int des(int fd)
{
    int err, stat;

    err = 0;
    stat = desecb(fd);
    if (stat) err++;

    stat = symDESecb(fd, 4096, 3);
    if (stat) err++;

    stat = symDEScbc(fd, 4096, 3);
    if (stat) err++;

    if (err)
        return -1;
    else
        return 0;

}
