static const unsigned char bad_digest[] =
{
    0x12, 0xB8, 0x45, 0x7E,
    0xD6, 0xC0, 0x7e, 0x24,
    0x09, 0x7F, 0xAE, 0x9F,
    0xE3, 0x19, 0xE0, 0x97,
};



static unsigned char digest[16];


int testICV(int fd)
{
    HMAC_PAD_REQ hmacRQ;
    int          status, testfail;

    testfail = 0;


    memset(digest, 0, 16);
    memset(&hmacRQ, 0, sizeof(hmacRQ));


    hmacRQ.opId     = DPD_MD5_LDCTX_HMAC_PAD_ULCTX_CMP;

    hmacRQ.keyBytes = 18;
    hmacRQ.keyData  = (unsigned char *)hmac_key;

    hmacRQ.inBytes  = 810;
    hmacRQ.inData   = (unsigned char *)msg_in;

    hmacRQ.outBytes = 16;
    hmacRQ.outData  = digest;

    hmacRQ.cmpData  = (unsigned char *)exp_digest;


    status = ioctl(fd, IOCTL_PROC_REQ_BLOCK_VIRTUAL, (int)&hmacRQ);

    if (status)
    {
        if ((status == SEC2_INVALID_CHA_TYPE) ||
            (status == SEC2_CHA_ERROR))
        {
            consolemsg("hash", "testICV", "", "not supported");
            return 0;
        }
        else
        {
            printf("hash: testICV - driver error with 0x%08x\n", status);
            return -1;
        }
    }

    /* compare result */
    if (memcmp(exp_digest, digest, hmacRQ.outBytes) == 0)
        consolemsg("hash", "testICV", "hmac md5 ICV match test", "OK");
    else
    {
        consolemsg("hash", "testICV", "hmac md5 ICV match test", "error");
        printf("expected digest:\n");
        dumpm((unsigned char *)exp_digest, hmacRQ.outBytes);
        printf("actual digest:\n");
        dumpm(digest, hmacRQ.outBytes);
        testfail++;
    }




    /* just point the compare data to a digest that's not quite right */
    hmacRQ.cmpData  = (unsigned char *)bad_digest;

    status = ioctl(fd, IOCTL_PROC_REQ_BLOCK_VIRTUAL, (int)&hmacRQ);

    /* In this case, a CHA error means an ICV mismatch... */
    if (status == SEC2_CHA_ERROR)
        consolemsg("hash", "testICV", "hmac md5 ICV mismatch test", "OK");
    else
    {
        consolemsg("hash", "testICV", "hmac md5 ICV mismatch test", "error");
        testfail++;
    }


    if (testfail)
        return -1;

    return 0;
}





int hash(int fd)
{
    int err, stat;

    err = 0;
    stat = testHash(fd);
    if (stat) err++;

    stat = testHmac(fd);
    if (stat) err++;

    stat = testICV(fd);
    if (stat) err++;

    if (err)
        return -1;
    else
        return 0;

}
